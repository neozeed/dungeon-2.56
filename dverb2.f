       COMMON /FINDEX/ BTIEF,BINFF
       COMMON /FINDEX/ RVMNT,RVCLR,RVCYC,RVSND,RVGUA
       COMMON /FINDEX/ ORRUG,ORCAND,ORMTCH,ORLAMP
       COMMON /FINDEX/ MDIR,MLOC,POLEUF
       COMMON /FINDEX/ QUESNO,NQATT,CORRCT
       COMMON /FINDEX/ LCELL,PNUMB,ACELL,DCELL,CPHERE
C CXAPPL, PAGE 2
C
       CXAPPL=0 
C NO RETURN.
       IF(RI.EQ.0) RETURN  
C IF NO ACTION, DONE.
       GO TO (1000,2000,3000,4000,5000,6000,7000,
     1       8000,9000,10000,11000,12000,13000,14000),RI
       CALL BUG(5,RI)
C
C C1- COFFIN-CURE
C
1000       EGYPTF=OADV(COFFI).NE.WINNER   
C T IF NO COFFIN.
       RETURN
C
C C2- CAROUSEL EXIT
C C5- CAROUSEL OUT
C
2000       IF(CAROFF) RETURN  
C IF FLIPPED, NOTHING.
2500       CALL RSPEAK(121)  
C SPIN THE COMPASS.
5000       I=XELNT(XCOND)*ZRND(8)  
C CHOOSE RANDOM EXIT.
       XROOM1=ZAND((TRAVEL(REXIT(HERE)+I)),XRMASK)
       CXAPPL=XROOM1 
C RETURN EXIT.
       RETURN
C
C C3- CHIMNEY FUNCTION
C
3000       LITLDF=.FALSE.  
C ASSUME HEAVY LOAD.
       J=0
       DO 3100 I=1,OLNT  
C COUNT OBJECTS.
         IF(OADV(I).EQ.WINNER) J=J+1
3100       CONTINUE
C
       IF(J.GT.2) RETURN  
C CARRYING TOO MUCH?
       XSTRNG=446 
C ASSUME NO LAMP.
       IF(OADV(LAMP).NE.WINNER) RETURN   
C NO LAMP?
       LITLDF=.TRUE. 
C HE CAN DO IT.
       IF(ZAND(OFLAG2(DOOR),OPENBT).EQ.0)
     1       OFLAG2(DOOR)=ZAND(OFLAG2(DOOR),ZNOT(TCHBT))
       RETURN
C
C C4-       FROBOZZ FLAG (MAGNET ROOM, FAKE EXIT)
C C6-       FROBOZZ FLAG (MAGNET ROOM, REAL EXIT)
C
4000       IF(CAROFF) GO TO 2500  
C IF FLIPPED, GO SPIN.
       FROBZF=.FALSE. 
C OTHERWISE, NOT AN EXIT.
       RETURN
C
6000       IF(CAROFF) GO TO 2500  
C IF FLIPPED, GO SPIN.
       FROBZF=.TRUE. 
C OTHERWISE, AN EXIT.
       RETURN
C
C C7-       FROBOZZ FLAG (BANK ALARM)
C
