       IF(J.LT.0) K=-8
       IF((((IABS(J).EQ.1).OR.(IABS(J).EQ.8)).OR.
     1   ((CPVEC(CPHERE+K).EQ.0).OR.(CPVEC(NXT-K).EQ.0))).AND.
     2    (CPVEC(NXT).EQ.0)) GO TO 14500 
C CANT DO IT?
       RETURN
